int	ft_str_is_alpha(char *str)

{
	int	i;
	int	r;

	i = 0;
	r = 1;
	while (str[i] != '\0')
	{
		if ((str[i] >= 'A' && str[i] <= 'Z')
			|| (str[i] >= 'a' && str[i] <= 'z'))
			r = 1;
		else
		{
			r = 0;
			break ;
		}
		i++;
	}
	if (str [0] == 0)
		r = 1;
	return (r);
}
